    <script>
        function validateForm(event) {
            event.preventDefault(); // Prevent form submission

            // Get form data
            const form = event.target;
            const name = form.full_name.value;
            const email = form.email.value;
            const service = form.service.value;
            const serviceDate = form.service_date.value;
            const specialRequest = form.special_request.value;
            const contactNumber = form.contact_number.value;

            // Set modal content
            document.getElementById('confirm_name').textContent = name;
            document.getElementById('confirm_email').textContent = email;
            document.getElementById('confirm_service').textContent = service;
            document.getElementById('confirm_date').textContent = serviceDate;
            document.getElementById('confirm_request').textContent = specialRequest;
            document.getElementById('confirm_number').textContent = contactNumber;

            // Fetch service price
            fetch(`fetch_service_price.php?service=${encodeURIComponent(service)}`)
                .then(response => response.json())
                .then(data => {
                    if (data.error) {
                        document.getElementById('confirm_number').textContent = "Date not available";
                    } else {
                        document.getElementById('confirm_number').textContent = `$${data.contact_number}`;
                    }

                    // Show modal
                    $('#confirmationModal').modal('show');
                })
                .catch(error => {
                    console.error('Error fetching service number:', error);
                    document.getElementById('confirm_number').textContent = "Error fetching number";
                    $('#confirmationModal').modal('show');
                });

            // Save button action
            document.getElementById('saveButton').onclick = function() {
                const contactDate = document.getElementById('contact_dateSelect').value;

                if (contactDate === "Select Contact Date") {
                    alert("Please select a contact date.");
                    return;
                }

                // Submit the form
                form.submit();
                document.getElementById('alertMessage').classList.remove('d-none'); // Show alert
                setTimeout(function() {
                    window.location.href = "index.html"; // Redirect after form submission
                }, 1000);
            };
        }
    </script>
